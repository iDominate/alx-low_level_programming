
/**
 * op_add - add 
 * @a: int
 * @b: int
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b); 
}

/**op_sub - subtract
 *
 * @a: int
 * @b: int
 *
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul
 * @a: a
 * @b: b
 *
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 *
 * @a: a
 * @b: b
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod
 *
 * @a: a
 * @b: b
 *
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
